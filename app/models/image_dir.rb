  key :name, String
  key :path, String
  timestamps!
  
  def convert_to_image_list
    il = ImageList.create(:name => self.name, :chapman_code => nil)
    il.image_files = self.image_file
    il.save!
    il
  end
  
  
  def log(msg)
    self.upload.log(msg)
  end
